package Loops;

public class WhilePractice2 {
    public static void main(String[] args) {
        /*
        from age of 18 to 25 (inclusive)
        "My age is ..."
         */
        int age = 18;
        while (age <= 25) {
            System.out.println("My age is " + age);
            age++;
        }
        // print number between 30 and 45 (inclusive)
        int number = 30;
        while (number <= 45) {
            System.out.println("The number is " + number);
            number++;

        }

    }
}
